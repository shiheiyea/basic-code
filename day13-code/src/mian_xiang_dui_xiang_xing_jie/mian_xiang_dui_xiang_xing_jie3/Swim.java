package mian_xiang_dui_xiang_xing_jie.mian_xiang_dui_xiang_xing_jie3;

public interface Swim {
    /*接口中成员的特点
        成员变量
                只能是常量
                默认修饰符: public static final
        构造方法
                没有
        成员方法
                只能是抽象方法
                默认修饰符:public abstract
        JDK7以前: 接口中只能定义抽象方法。
     */
    public abstract void Swim();
}
